當災害發生時，當地社區的慈濟志工即主動勘災，評估後續可能需要的物品，視災況適時再由其他功能或專業職志工投入賑災。

第一時間，我們即時走入第一線災區，迅速發放物資、熟食、購物卡與慰問金，並提供臨時庇護與防疫支援等，疫情之時，我們於全球發放超過 5,070 萬件醫療防疫物資，讓 88% 志工與職工更能感受到平靜且更能應對生活變化。

我們在烏克蘭難民最迫切需要的時刻抵達波蘭，援助烏克蘭難民 478,980 人次，讓 88% 的烏克蘭難民降低心理衝擊。而隨著災後生活逐步重建，我們亦持續提供心理支持與生活協助，有 75% 的烏克蘭難民能穩定基本生活，待身心安穩後，更進一步銜接經濟賦能與工作媒合，幫助 57% 的難民改善經濟狀況，重新建立對未來的安全感與經濟自立能力。災難發生時，我們伸出援手，以家人般的陪伴支持受助者重建生活、恢復自立；當生活重回軌道，若再次遇到挑戰，這些曾受助的人們更能彼此支持，匯聚成「以善啟善」的持續動能。

## （二）學習啟信 — 從受限的學習到構築未來的信心

我們深知，天災人禍往往讓學習的場域、時間與資源被迫縮減，甚至中斷。為了讓國際青年不因環境受限而停下腳步，我們招募線上伴學志工，提供長期且穩定的陪伴與支持。例如：陪伴敘利亞難民、泰緬孤軍後代，學習華語文、程式語言等專業技能。三年間，國際青年伴學計畫推展至 7 個國家，並協助 114 名小學伴取得華語文證照。隨著知識與能力逐步累積，小學伴的自信也同步提升，有高達 88% 的小學伴提升了學習動機，展現出更強的好奇心與成就感，進而開始相信「我學得到、也做得到」，把所學應用於實際情境中，在學以致用的過程中累積實務知能。當學習與生命經驗產生連結，我們關注的不只是補上落差，更是小學伴們是否能重新看見自己的價值，為未來開啟更多可能。

## （三）利他動能 — 從人際的孤島到共根的社群

在受災地區或資源有限的國家，我們致力以「利他精神」牽起跨種族、跨宗教、跨語言、跨階級的連結。三年間，我們和波蘭天主教靈醫會、波蘭婦女基金會合作開設波蘭文課程，授課幫助烏克蘭難民超過 66,994 人次，其中有 69% 的烏克蘭難民更有信心融入波蘭社會；當人們在困境時曾被支持，也更願意把這份力量再傳出去，80% 的烏克蘭難民更願意付出行動去幫助他人。

在國際青年伴學計畫中，我們招募有意願付出愛心的臺灣志工擔任大學伴，並給予紮實的志工培訓，即使是透過線上的方式助人，仍有 92% 的大學伴在服務後提升了心靈滿足感，更能感受到人際歸屬，87% 的大學伴進一步強化助人的信念；同時，90% 的小學伴在陪伴和支持的學習環境下，也更願意付出行動、幫助他人。我們看見，人與人之間消除隔閡並建立互信關係，無論是助人者或受助者都在善的交流中獲得滋養，讓這份「利他」精神成為能跨過藩籬的橋樑，讓全世界的人們連結為共根社群，彼此滋養、共生共榮。

在全球各地的人道救援行動中，慈濟串連跨國志工與在地社區組織，秉持「大愛無國界」的精神，鼓勵受助者與助人者之間的互助，為長遠的社會修復奠定更穩固的基礎。

