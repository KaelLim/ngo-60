### 滿納海之愛

敘利亞內戰自 2011 年爆發，始於「阿拉伯之春」後的社會動盪，並在多年衝突中造成大量傷亡與數百萬人流離失所。在難民跨境安置的過程中，「教育中斷」往往成為最容易被延後處理、卻對下一代影響最深的缺口。以土耳其為例，當地長期收容數百萬敘利亞人，在落腳異地的過程中，教育成為被忽視的重大議題。

為避免失學孩童因生計淪為童工，我們於 2015 年成立滿納海國際學校，「滿納海」在阿拉伯語中意思是「沙漠的泉源」，象徵在困境中守住學習與希望。延續創校的初衷，我們招募大學伴透過線上陪伴小學伴（敘利亞難民學子）學習華語，更於寒暑假期間，安排臺灣大學伴前往土耳其，促進大小學伴彼此的文化交流與學習，讓教育不只回到課本，也回到生活與視野。

### 異域之愛（泰北地區華語文伴學）

自 1994 年起，我們在泰北清邁、清萊以及夜豐頌省等地，陪伴流亡異域的孤軍後代，逐步推動聚落安居、農業改良、安養照護、貧戶關懷與助學支持等。當扶困工作逐步走向穩定，證嚴法師強調教育是讓地區永續自立的關鍵，因此歷經 8 年籌備與興建，泰北清邁慈濟學校終於在 2005 年正式開班。為了讓小學伴能夠開拓海外學習和發展的機會，學校課程重視泰語、英語和華語共三種語言的教育，我們也結合國際青年伴學計畫，牽起在臺灣的大學伴陪伴在泰北的小學伴學習華語文，同時支持有意願返泰北成為華語師資的學生來臺升學，逐步補足在地教育人才缺口，讓教育支持轉化為可回流、可延續的在地師資動能。

### 非常青年（非洲地區線上陪學）

「非常青年」以多年期跨國線上專案形式，號召大學伴陪伴南非、納米比亞、尚比亞、辛巴威與莫三比克等地學員，強化溝通素養與領導力，培育具信念與行動力的新世代青年。計畫以多學期、主題式課程系統推進，涵蓋心靈啟發、品格養成、溝通表達與實踐能力培訓，引導青年從自我覺察走向外在行動，逐步建立自信、毅力與勇氣，並將學習能量導入社區服務。其亮點不只在個人成長，而在「線上培育 × 在地行動」的接力：不少學員已主動投入公益行動（如課業輔導、環保倡議、青年共學社群），讓「教育即力量」在非洲社區形成善的循環。

### 莫三比克伴學

莫三比克於 1992 年結束長達 18 年的內戰，戰後重建緩慢，長期貧窮與教育資源不足交織；班級人數動輒上百、教材與合格師資不足等問題普遍存在。成人識字率約六成，意味仍有相當比例的成年人缺乏基礎讀寫能力。此外，中北部地區亦曾受 2019 年氣旋「伊代」重創。多重風險交疊，使「教育中斷」與「基本生活不穩」相互強化。

因此，我們承諾在莫三比克推動校舍、耐災住屋與健康支持等重建工作，並招募、培訓臺灣志工展開伴學計畫，在首都馬普托與索法拉省推行「小組制」線上伴讀，陪伴在地學子學習華語與英語，同時融入靜思語的人文素養學習；也支持部分學子銜接到慈濟大學秋季班留學，讓教育支持形成可延伸的培力鏈。

