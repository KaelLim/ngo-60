## 影響力邏輯模型（Logic Model）

本報告書採用「影響力邏輯模型」(Logic Model) 作為核心方法論。影響力邏輯模型是國際永續發展及影響力揭露領域中，最具代表性的影響力架構之一，其廣泛被聯合國、世界銀行、經濟合作暨發展組織、聯合國兒童基金會、洛克斐勒基金會、福特基金會等組織採用。其當代的應用形式主要始於 1995 年美國社會科學家 Carol Weiss 撰文推廣。影響力邏輯模型強調「以終為始」，清楚描繪組織如何投入資源、策劃活動及擬定策略，逐步帶來可衡量的社會改變。其精神並非停留在單一計畫的成果。對我們而言，這不只是科學的評估工具，更是對大眾「誠正信實」的徵信表現，協助我們看見每一分努力如何為社會創造深遠的價值。

## 五個主要環節

影響力邏輯模型包括五個主要環節：

1. **事件或現象（Event）**：指專案所希望解決的問題，以及背後的痛點。
2. **活動（Activity）及投入（Input）**：指我們採取的行動及投入的資源；傳統的影響力邏輯模型多為先盤點投入資源，再展開行動；對慈濟而言，當事件發生，採取行動是刻不容緩，在前往第一線的過程中，發揮「以善啟善」的動能，進而匯聚更多社會善心與資源挹注。
3. **產出（Output）**：指紀錄活動後直接帶來的短期可量化結果。
4. **成果（Outcome）**：對應中長期的改變。
5. **影響（Impact）**：為環境、社會、經濟等面向創造的長期願景。

透過影響力邏輯模型，我們以系統化的方式呈現行動背後的邏輯，使每一分善念、每一項行動，都能在更清晰的框架下展現其具體影響。

