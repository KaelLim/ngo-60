## 身心同行的永續環境

慈濟環保志業的起點，源於一場具足因緣的呼籲。1990 年 8 月 23 日，證嚴法師應邀至臺中新民商工參加講座。清晨行腳途中，看見夜市收攤後街道滿是垃圾，地面髒亂不堪。遂於演講結束時，證嚴法師感受到那雙雙充滿熱情的手若只停留在鼓掌歡呼，實在可惜，便呼籲：「請大家把鼓掌的雙手，用在撿垃圾、掃街道、做資源回收，讓我們這片土地變成淨土；垃圾變黃金，黃金變愛心。」證嚴法師一句輕輕的呼籲，開啟了長達數十年的行動起點。住在臺中豐原的楊順苓小姐率先響應，從社區巷弄開始推動資源回收、鄰里之間一戶一戶勸說，邀請大家把日常生活中看似無用的瓶瓶罐罐集中起來，身體力行闡揚「惜福愛物」的觀念。自此，「用鼓掌的雙手做環保」成為我們集結眾人做環保的開端。

我們相信，真正的富足與安樂並不在於不斷擴張物質佔有，而在於能否知足少欲，不被五欲所牽引。證嚴法師長期提醒大眾：「知足、感恩、善解、包容」，並常以「惜福愛物」提醒弟子與志工，物品都有其緣起與物命，任意浪費就是對大地與眾生的傷害。若能從心念上減少不必要的消費與欲求，生活自然趨於簡樸清淨，對地球的壓力也隨之減輕。

「環保」不只是把垃圾做好分類，更是以知足之心調柔自己，以惜福之行護念萬物，都是在實踐佛陀所說的知足之道，也是對地球、生態與未來世代的一份責任。在證嚴法師呼籲之後的三十多年裡，我們環保志業逐步發展為兼具身心照顧、世代教育與全球關懷的行動網絡。

## 社區網絡的深耕共伴

1996 年賀伯颱風帶來嚴重的水患，慈濟志工秉持「跑在最前，做到最後」的精神，在災後半個月內動員上萬人次投入清理家園、發放物資與陪伴居民。證嚴法師走訪災區時，看見許多家庭平日關係疏離、弱勢者缺乏支持，故於災時更顯孤立無援。

證嚴法師體悟到，唯有「就近才能救急」，遂提出「社區志工」理念，將志工依居住地重新編組，使志工關懷能更深入社區日常。以「敦親睦鄰、守望相助」為核心，讓社區在平日就建立互信與連結，使隱性需求被看見、弱勢族群能得到即時的支持與陪伴，在挑戰來臨時，社區能迅速啟動互助網絡，成為受災者最堅實的後盾。

我們相信社區是「人與人互為善緣」的場域，志工並非僅是單向資源的提供者，更是走入生命，在鄰里中傳遞大愛精神的陪伴者。志工透過走入鄰里、傾聽需求、陪伴家庭，使「互為依靠、守望相助」的精神在每個家戶中扎根，讓社區從陌生走向熟悉，從疏離走向共同生活的力量。

## 人道救援到翻轉生命希望

1990 年代，證嚴法師帶領慈濟跨越國界，開展橫跨全球的關懷足跡。面對飽受天災、人禍與貧困磨難的受災者，我們秉持著「無緣大慈，同體大悲」的心念。給予急難物資的下一步，更關鍵的是協助受災者重拾生活信心與生命的尊嚴。透過「取之當地、用之當地」的實踐，我們陪伴受助者發揮自助助人的良能，帶動在地居民共同參與家園重建，將受助之餘轉為自力更生的動力。

親自走入苦難現場，透過陪伴、膚慰受創身心並協助重建家園，是我們人道救援自立利他的重要環節，落實「安身、安心、安生」的關懷，陪伴受災者轉苦為樂。當鄰里居民能相互扶持，社區共同面對考驗與無常，將受災土地轉化為匯聚種人善念的園地，讓救援的終點不只是災後的安頓，而是引領受助者翻轉生命，邁向自力更生、重拾尊嚴且能造福人群的福慧人生。

## 以三個改變階段描繪正向影響力

我們三大影響力主軸路徑，收斂為「自覺自度」、「勤學增能」、「眾緣合和」作為內在脈絡，從一個人的善念，如何延展成爲社會與世界的良善循環。

### 自覺自度

「自覺自度」是影響力的起點，透過陪伴弱勢、關懷生命，我們先協助受助者穩定衣食溫飽與安心生活的根基。當生活趨於安穩，心念自然轉為柔軟，更能看見身邊的苦難與需要，這份自覺啟動受助者「重新站起來」的內在力量，實踐翻轉生命的可能，同時也讓投入其中的志工在實踐中增長慈悲心。

### 勤學增能

在善念萌芽後，更需透過能力養成來延續行動力。「勤學增能」便在此時開展，透過深度陪伴、人文教育、專業訓練，培養受助者自立的能力，並引導其學習友善環境的生活方式，培養在生活與社會中跨越困境的力量；同時，我們亦致力於培養志工的專業素養，使其具備應對多元需求的服務技能。讓每一份援助不只是短暫的物資給予，而是能發揮社會正向的影響力。

### 眾緣合和

當善念與能力逐漸成熟，便自然匯聚成「眾緣合和」的力量。鄰里、社區與跨域夥伴因良善而團結，成為支持彼此的力量，使災後的家園得以重建、弱勢家庭不再孤單，善意從一個人擴大到一群人，最終走向全世界。這三個改變階段環環相扣、承先啓後，共同構成慈濟推動影響力、深耕社會韌性的核心方法。

