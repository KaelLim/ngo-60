## 專案內容與亮點

自 COVID-19 疫情爆發以來，我們看見需求將防疫物資（隔離衣、口罩、手套、護目鏡等）送達急需的國家或地區，並啟動在地的安貧紓困。證嚴法師叮嚀「瘟疫是大災難，要全球發心、全球發放，給予人人充足的保護。」本土疫情尚未升溫以前，我們已展開不分國界的物資馳援，只為讓需要的人們都能受到充分保護。2021 年，隨著臺灣本土疫情升溫，社會對防疫保護的需求急速上升，我們在第一線看見迫切的缺口：青年是國家的未來及希望，但是需要面對人群的年輕工作者卻因施打順序較後，必須面對人群而暴露在風險之中，同時承受著疫情帶來的不確定與心理壓力。基於「看見需求、即刻援助」的心念，我們認為需要回應社會迫在眉睫的需要，只要能讓更多人獲得保護力的行動，都值得被啟動。因此，我們選擇積極促成疫苗採購，引入更多可供全民接種的疫苗。另一方面，延續慈濟人多年的賑災經驗，迅速展開多面向的防疫援助：在民生支持上，關懷染疫者與弱勢家庭，我們提供個人與家庭能安心防疫的物資（蔬果箱、安心箱）；在醫療支持上，我們提供醫療院所急需的醫療設備（氧氣槽、製氧機、呼吸器、救護車等）。同時，透過物資發放，我們也更早辨識出高風險家庭，進一步擴大關懷網絡。即使疫情壓力沉重，所有第一線志工的努力和無數個誠心護念，讓我們在困境中看見希望的微光。

疫情帶來的不僅是疾病，還有失業、失學的恐懼，為了回應這些挑戰，我們重啟已結案家庭的訪視，在疫情發生時提供物資援助，協助染疫者穩定生活，並重新確認這些家庭的需求。在疫情最嚴峻的時刻，法親、志工與職工同樣面臨染疫風險與生活壓力，因此我們啟動內部關懷機制，讓每一位法親、志工與職工在付出時，亦能獲得足夠的防疫物資，以及有必要的心理支持。照顧社會需求的同時，我們也照顧每一位發願的有心人，讓彼此互相守護，才能在風雨中依然緊密相連。

## 影響力成果

面對突如其來的疫情，我們提供的「安心物資箱」備有米、麵等糧食，以及靜思精舍製作的書籍刊物等，除了提供生活所需，也傳遞證嚴法師的祝福，幫助人們在不確定的生活中找到安定的力量。此外，定期的線上佛法開示，讓染疫者能在困境中安頓心念、重整步伐，面對生活的挑戰。三年間的調查顯示，有 90% 的染疫職工與志工降低了心理衝擊，更能感受到平靜並面對生活變化，改善幅度平均超過 81%。在此期間，證嚴法師呼籲大家「時時戒慎虔誠，這是人類的大哉教育」，引領大眾感念大地滋養萬物、戒慎虔誠。

證嚴法師慈示，災疫實為大自然對人類的示警。從 SARS 到 COVID-19，許多重大的疫情皆與人類過度開發、捕食動物的行為息息相關。我們相信環境的修復始於人心的自覺。當人人抱持感恩心，感恩大地生長萬物並落實「推素、愛物」，與眾生和諧共處，才有機會從根源消弭疫災風險。這份從「心」出發的改變，正是慈濟在疫情中推動最具深遠意義的社會影響力。

隨著疫情升溫，醫療量能瀕臨失衡，除了關心染疫者，我們更看見第一線的醫療守護者正承受巨大壓力，許多醫療院所陸續傳出防疫物資短缺的訊息，我們在最短時間內採購並送達製氧機、呼吸器、救護車等醫療器材，支持各大醫療院所在日夜不分的付出中仍能穩定運作。援助不僅止於臺灣，也擴及全球；在疫情期間送出了防護/隔離衣 1,338,938 件、口罩 11,793,314 件、防疫手套 9,033,100 件、面罩/護目鏡 524,922 件，提供給全球急需的醫療院所與檢疫單位，並依各地區需求，額外捐贈呼吸器、額溫槍、試劑、保暖衣物、環保毛毯、福慧珍粥、香積飯、香積麵、霧化機等 445,832 件。在全球共同面對疫情衝擊的時刻，只要有人需要，我們不分國界、始終與醫療夥伴並肩。

從照顧個體到支持醫療體系的過程中，我們也看見許多青年因無法及時接種疫苗而暴露於風險之中，承受著巨大壓力。為守護缺乏防護力相對不足的青少年，我們克服萬難採購 500 萬劑 BNT 疫苗，優先提供 12–17 歲青少年施打。即使採購過程歷經重重關卡，我們仍堅持完成，並在疫苗引進後的第一時間，開放各地靜思堂作為大型接種站，協助地方醫療院所疏導人潮、提升接種效率。靜思堂莊嚴寬敞、動線平順，並由志工提供協助與陪伴，使疫苗接種者在緊張的疫情中，仍能感受到溫暖與秩序。

即使疫情要求保持安全社交距離，但關懷也從未停擺。我們重啟聯繫所有曾關懷但已結案的家戶訪視，以物資箱配送為起點，主動詢問這些家戶是否有新的需求，讓每一個需要幫助的家庭都不被遺漏，在此過程中，我們重新將部分已結案的關懷戶重新納入長期追蹤名單，強化社會安全網。

綜觀整體行動，我們以「穩定個體」為起點，進一步「支援醫療體系」讓第一線醫護能安心付出；再把關懷延伸至「青年族群」補足關鍵防護缺口；最後透過「家戶追蹤」修補社會安全網，不因社會安全距離而阻斷連結，讓每一個人都不被遺落。

