儘管現行政府公共支持體系已有相當的基礎，但仍有力猶未逮之處，部分特殊處境的家庭隱於社會安全網的邊緣暗角，在求援無門中陷入困境。我們秉持「聞聲救苦，主動馳援」的慈悲初心，走進全臺 64%（235 個）鄉鎮市區，提供長者與脆弱家庭「全人、全家、全面、全程、全方位、全社區」的「六安」照顧。透過「安穩家園，美善社區」（安美）專案，志工主動發掘社會中隱蔽的暗角，深入家戶完成 7,943 戶修繕，確保每位長者與孩子都能住得安全且安心。

