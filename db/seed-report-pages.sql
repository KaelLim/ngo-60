## （二）學習發展 — 從迷茫乏力到學以致用的付出

對於逆境學子而言，穩定學習是翻轉生命的關鍵，他們不僅要克服生活環境的挑戰，還需付出更多心力重建自信心與人際關係，若缺乏支持，其容易失去學習動力，不敢或沒有機會選擇更適合自己的未來。

因此，我們以「新芽獎學金」勵學，三年間累積新芽獎學金新台幣逾 1 億 8 千萬元，激勵學子養成跨越困境的生命能量，共計 27,210 人次學子得以穩定就學，志工群更攜手陪伴孩子安心就學、持續成長，並培養自信與社會連結，讓每位學子都有機會如種子般長出新芽、逐漸茁壯，進而回饋社會。

延續新芽獎學金精神，我們推動多元青年培力課程，主題涵蓋團隊合作、時間管理、生涯規劃、服務學習與慈濟人文等，協助學子強化動機及累積知能，獲得面對未來的底氣。目前已協助 698 位青年學子參與培力課程，89% 的參與者同意其學習動機提升，90% 的參與者認同自身心靈滿足感增加，並建立穩定的人際連結與歸屬感；另外，透過在地服務與學習，更有 93% 的參與者更願意付出行動去幫助他人。當學子從服務中看見自己的價值，將更有機會對世界懷抱一份善意，在我們共築的信任及歸屬感中持續學習與成長。

## （三）社區共生 — 從獨自支撐到攜手守護社區

臺灣位處於環太平洋地震帶與颱風主要路徑上，氣候變遷之下面臨多重且複合的災害風險，因此強化社區的防災韌性與應變能力至關重要。我們在全臺 95% 的縣市中，培訓了 15,870 位防災先行者，使他們具備必要的防災知識與信心，為社區建立自主防災與災後復原的能量。其中，93% 的受訓學員自認提升了防災與災害應對的能力，在日常中更頻繁地與他人分享或討論防災知識，也更有信心能面對災害，並且更願意投入災後復原工作。學員在受訓過程中，意識到自己在社區中並非獨自面對風險，而是能連結社區內其他資源單位，共同參與和擔任防救災的先行者，使社區增強自我照顧與修復的能力。

