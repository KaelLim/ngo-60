## 影響力成果

在烏克蘭戰爭發生的初期，我們透過多面向的救援行動，提供收容所、生活物資與心理輔導服務，協助難民在高度不確定的情境中穩定身心狀態。88% 的難民在接受救援後降低心理衝擊，內心更能感到平靜與快樂，並認為自己在面對災難事件時，更有能力應對生活上的變化；情緒和心理健康狀況平均提升超過 80%。在天災人禍交織的艱難時期，我們號召各方志工投入救援行動，讓難民在第一時間感受到支持與安心。

購物卡和現金卡雖然能解決生活上的燃眉之急，但我們也看到了烏克蘭難民在波蘭生活的困境：語言能力直接影響就業狀況，更是難民融入當地社區的基本門檻，另一方面，許多難民在家鄉曾經擁有穩定收入與社會地位，在波蘭卻需要仰賴補助生活，在經濟自立的過程中容易因著巨大的落差感而使人退縮。因此，我們和波蘭婦女基金會、天主教靈醫會合作，透過波蘭語培力與就業支持，協助難民更有信心展開求職。75% 的難民能夠滿足醫療、生理、健康等生活基本需求，滿足程度提升超過 70%，基本生理需求（飲食、居住等）的提升程度更超過 75%。照顧好生活條件後，進一步關注的是長期的經濟穩定，57% 的居民認同提升了經濟狀況，且平均提升超過 66%，其中「更有信心可以在當地找到一份適合的工作」更提升了超過 70%。實際上，受助難民平均每月提升了 2,378 PLN 波蘭茲羅提（約等同新臺幣 1.8 萬多元），在提升就業能力上有明顯的成效。

除了滿足生活基本所需以及穩定經濟基礎，我們進一步陪伴難民建立融入波蘭的生活方式。我們和天主教靈醫會、波蘭婦女基金會開設波蘭文課程、社區推廣班，創造烏克蘭難民與當地社區居民的交流空間，並且在互動中找到在波蘭當地人際支持，有 69% 的居民提升了人際互動，其中以「對於自己能夠融入當地（波蘭）社會的信心」提升幅度更超過 74%。這些支持讓難民能夠在異地生活時持續地提升自立信心，並相信自己能夠重新建立穩定的生活與經濟基礎。

我們在當地發現有難民具備剪髮專長，便邀請難民為其他人義剪；有人擅長翻譯，將靜思語翻譯成波蘭語，傳遞溫暖。即使不是長期志工，也讓難民在各式各樣的活動中發揮專長，約 80% 的居民認同接受救援後，更願意付出行動去幫助別人；在助人後的正向感受、分享助人重要性的意願，以及公共參與的主動性等指標上平均提升超過 78%，其中「更願意和別人分享助人的重要性」更提升超過 82%。我們透過設計可參與的助人角色與互動場域，讓「被幫助」轉化為「幫助別人」，重新找回自我價值，並形塑更具韌性的支持網絡。

